    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        String jenis = null;
        if(rLaki.isSelected()){
            jenis = "Laki - laki";
        }else if(rPrmpn.isSelected()){
            jenis = "Perempuan";
        }
        try{
            String sql = "Update pelanggan set nama_pelanggan=?,jenis_kelamin=?,no_telp=?,alamat=? where id_pelanggan='"+txID.getText()+"'";
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txNama.getText());
            stat.setString(2, jenis);
            stat.setString(3, txNoTelp.getText());
            stat.setString(4, txAlamat.getText());
            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");
            kosong();
            txID.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah"+e);
        }
        datatable();                               
    }//GEN-LAST:event_btnUbahActionPerformed

